package Variables;

public class Variables {
    public static void main(String[] args) {
        // Variables
        int edad = 19;
        boolean disponible = true; // true/false
        char genero;
        genero = 'm';


        // Access to variable
        System.out.println(edad);


        // Edit Value
        edad = 20;
        System.out.println(edad);
        System.out.println(disponible);
        System.out.println(genero);
    }
}
